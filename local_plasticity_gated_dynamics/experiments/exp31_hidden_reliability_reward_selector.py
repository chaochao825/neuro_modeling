"""Hidden-demand, executed-reward-only actuator selection under memory pressure.

Exp31 removes the explicit actuator mixture and oracle demand coordinate used by
Exp30.  A block hides the reliability of a direct cue.  The alternative fixed
actuator retrieves a random value from dense key--value writes whose accuracy
degrades with association load and distractor writes.  A two-action local
delta rule receives only the scalar reward generated by its executed probe
action, then freezes one actuator for the rest of the block.

The primary utility is full-block accuracy, including forced exploration.
No high-rank carrier is claimed in this isolated controller-identifiability
test, and no autograd or BPTT is used.
"""

