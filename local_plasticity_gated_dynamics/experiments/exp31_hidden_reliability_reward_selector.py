def _file_sha256(path: Path) -> str:
    digest = hashlib.sha256()
    with path.open("rb") as stream:
        for chunk in iter(lambda: stream.read(1024 * 1024), b""):
            digest.update(chunk)
    return digest.hexdigest()


def _source_provenance(config: Mapping[str, Any]) -> dict[str, Any]:
    files = {
        name: _file_sha256(PROJECT_ROOT / name) for name in CRITICAL_CODE_FILES
    }
    config_path = Path(str(config.get("config_path", "")))
    git: dict[str, object] = {"commit": None, "tree": None, "dirty": None}
    try:
        commit = subprocess.run(
            ["git", "rev-parse", "HEAD"],
            cwd=PROJECT_ROOT,
            check=True,
            capture_output=True,
            text=True,
            timeout=5,
        ).stdout.strip()
        tree = subprocess.run(
            ["git", "rev-parse", "HEAD^{tree}"],
            cwd=PROJECT_ROOT,
            check=True,
            capture_output=True,
            text=True,
            timeout=5,
        ).stdout.strip()
        status = subprocess.run(
            ["git", "status", "--porcelain"],
            cwd=PROJECT_ROOT,
            check=True,
            capture_output=True,
            text=True,
            timeout=5,
        ).stdout
        git = {"commit": commit, "tree": tree, "dirty": bool(status.strip())}
    except (OSError, subprocess.SubprocessError):
        pass
    return {
        "schema_version": EVIDENCE_SCHEMA_VERSION,
        "protocol_version": PROTOCOL_VERSION,
        "critical_file_sha256": files,
        "source_config_sha256": (
            _file_sha256(config_path) if config_path.is_file() else None
        ),
        "git": git,
    }


def _task_config(config: Mapping[str, Any]) -> HiddenReliabilityTaskConfig:
    payload = dict(config["task"])
    for name in (
        "load_values",
        "distractor_write_values",
        "direct_reliabilities",
    ):
        payload[name] = tuple(payload[name])
    return HiddenReliabilityTaskConfig(**payload)


def _validate_config(config: Mapping[str, Any]) -> None:
    if config.get("protocol_version") != PROTOCOL_VERSION:
        raise ValueError("config protocol_version does not match Exp31")
    _task_config(config)
    seeds = seed_list(config["seeds"])
    if not seeds:
        raise ValueError("Exp31 requires at least one seed")
    if bool(config.get("used_autograd", False)) or bool(config.get("used_bptt", False)):
        raise ValueError("Exp31 local selector cannot use autograd or BPTT")
    selector = dict(config.get("selector", {}))
    if selector.get("update_rule") != "sample_average_scalar_reward_delta":
        raise ValueError("Exp31 requires the registered reward-only update rule")
    prior = float(selector.get("q_prior", 0.5))
    if not np.isfinite(prior) or not 0.0 <= prior <= 1.0:
        raise ValueError("selector.q_prior must lie in [0, 1]")


def _spec_dimensions(spec: HiddenReliabilityBlockSpec, mode: str) -> dict[str, object]:
    return {
        "block_id": spec.block_id,
        "cell_rep": spec.cell_rep,
        "direct_reliability": spec.direct_reliability,
        "association_load": spec.association_load,
        "distractor_writes": spec.distractor_writes,
        "condition": mode,
        "actuator_mode": mode,
        "protocol_version": PROTOCOL_VERSION,
    }


def _planned_conditions(config: Mapping[str, Any]) -> list[dict[str, object]]:
    specs = make_hidden_reliability_block_specs(_task_config(config), 0)
    return [
        _spec_dimensions(spec, mode)
        for spec in specs
        if spec.split == "test"
        for mode in MODES
    ]


def _action_prediction(outputs: CapacityLimitedOutputs, action: int) -> np.ndarray:
    if action == 0:
        return np.asarray(outputs.routing, dtype=np.float64)
    if action == 1:
        return np.asarray(outputs.associative, dtype=np.float64)
    raise ValueError("action must be routing=0 or associative=1")


def _training_dictionary(
    config: HiddenReliabilityTaskConfig,
    specs: tuple[HiddenReliabilityBlockSpec, ...],
    actuator: CapacityLimitedAssociativeActuator,
) -> tuple[str, dict[tuple[float, int, int], int], dict[str, float], str]:
    global_correct = np.zeros(2, dtype=np.float64)
    global_trials = 0
    cell_correct: dict[tuple[float, int, int], np.ndarray] = defaultdict(
        lambda: np.zeros(2, dtype=np.float64)
    )
    cell_trials: dict[tuple[float, int, int], int] = defaultdict(int)
    fingerprint = hashlib.sha256()
    for spec in specs:
        if spec.split != "train":
            continue
        block = materialize_hidden_reliability_block(config, spec)
        outputs = actuator.evaluate(block)
        if not outputs.update_budget_exact:
            raise RuntimeError("query-shuffled control changed the write budget")
        fingerprint.update(block.fingerprint.encode("ascii"))
        for action in range(len(ACTIONS)):
            prediction = _action_prediction(outputs, action)
            correct = float(np.sum(prediction == block.targets))
            global_correct[action] += correct
            cell_correct[spec.cell_key][action] += correct
        global_trials += block.n_trials
        cell_trials[spec.cell_key] += block.n_trials
    if global_trials < 1:
        raise RuntimeError("Exp31 training split is empty")
    global_accuracy = global_correct / float(global_trials)
    fixed_action = int(np.argmax(global_accuracy))
    oracle_map = {
        cell: int(np.argmax(correct / float(cell_trials[cell])))
        for cell, correct in cell_correct.items()
    }
    return (
        ACTIONS[fixed_action],
        oracle_map,
        {ACTIONS[index]: float(global_accuracy[index]) for index in range(2)},
        fingerprint.hexdigest(),
    )


def _online_predictions(
    *,
    targets: np.ndarray,
    outputs: CapacityLimitedOutputs,
    probe_schedule: np.ndarray,
    q_prior: float,
    tie_break_action: int,
    random_deploy_action: int,
) -> tuple[dict[str, np.ndarray], dict[str, object]]:
    n_trials = targets.size
    n_probe = probe_schedule.size
    local = RewardOnlyDeltaSelector(
        q_prior=q_prior, tie_break_action=tie_break_action
    )
    local_prediction = np.empty(n_trials, dtype=np.float64)
    random_prediction = np.empty(n_trials, dtype=np.float64)
    for trial, action in enumerate(probe_schedule):
        action = int(action)
        prediction = _action_prediction(outputs, action)[trial]
        local_prediction[trial] = prediction
        random_prediction[trial] = prediction
        local.observe(action, float(prediction == targets[trial]))
    receipt = local.receipt()
    local_prediction[n_probe:] = _action_prediction(
        outputs, receipt.selected_action
    )[n_probe:]
    random_prediction[n_probe:] = _action_prediction(
        outputs, random_deploy_action
    )[n_probe:]
    return (
        {
            "reward_only_local": local_prediction,
            "matched_probe_random": random_prediction,
        },
        {
            "receipt": receipt,
            "random_deploy_action": random_deploy_action,
        },
    )


def run_seed(
    config: dict[str, Any],
    seed: int,
    results_root: str | Path,
    *,
    run_label: str | None = None,
) -> Path:
    """Run one fully paired Exp31 seed and retain every registered test block."""

    _validate_config(config)
    if seed not in seed_list(config["seeds"]):
        raise ValueError("seed is not registered by the selected Exp31 config")
    initialize_seed(seed)
    task = _task_config(config)
    specs = make_hidden_reliability_block_specs(task, seed)
    actuator = CapacityLimitedAssociativeActuator(
        key_dim=task.key_dim, distractor_strength=task.distractor_strength
    )
    fixed_best, oracle_map, train_accuracy, train_fingerprint = _training_dictionary(
        task, specs, actuator
    )
    fixed_action = ACTIONS.index(fixed_best)
    provenance = _source_provenance(config)
    if config.get("profile") == "formal" and provenance["git"].get("dirty") is not False:
        raise RuntimeError("formal Exp31 requires a clean, Git-bound worktree")
    run_config = {key: value for key, value in config.items() if key != "config_path"}
    run_config["evidence_provenance"] = provenance
    q_prior = float(dict(config.get("selector", {})).get("q_prior", 0.5))

    with ExperimentRun(
        EXPERIMENT,
        seed,
        run_config,
        results_root=results_root,
        run_label=run_label,
    ) as run:
        run.register_conditions(_planned_conditions(config))
        for spec in specs:
            if spec.split != "test":
                continue
            try:
                block = materialize_hidden_reliability_block(task, spec)
                outputs = actuator.evaluate(block)
                if not outputs.update_budget_exact:
                    raise RuntimeError("query-shuffled write budget mismatch")
                probe_seed = derive_seed(seed, EXPERIMENT, "probe", spec.block_id)
                probe_schedule = balanced_probe_schedule(
                    task.probe_trials, seed=probe_seed
                )
                probe_digest = hashlib.sha256(
                    probe_schedule.astype("<i8", copy=False).tobytes()
                ).hexdigest()
                tie_action = int(
                    derive_seed(seed, EXPERIMENT, "tie", spec.block_id) % 2
                )
                random_action = int(
                    derive_seed(seed, EXPERIMENT, "random", spec.block_id) % 2
                )
                online, audit = _online_predictions(
                    targets=np.asarray(block.targets),
                    outputs=outputs,
                    probe_schedule=probe_schedule,
                    q_prior=q_prior,
                    tie_break_action=tie_action,
                    random_deploy_action=random_action,
                )
                receipt = audit["receipt"]
                oracle_action = oracle_map[spec.cell_key]
                predictions = {
                    "fixed_routing": np.asarray(outputs.routing),
                    "fixed_associative": np.asarray(outputs.associative),
                    "associative_query_shuffled": np.asarray(
                        outputs.associative_query_shuffled
                    ),
                    "train_fixed_best": _action_prediction(outputs, fixed_action),
                    "matched_probe_random": online["matched_probe_random"],
                    "reward_only_local": online["reward_only_local"],
                    "oracle_hidden_train_map": _action_prediction(
                        outputs, oracle_action
                    ),
                }
                selected_actions = {
                    "fixed_routing": 0,
                    "fixed_associative": 1,
                    "associative_query_shuffled": 1,
                    "train_fixed_best": fixed_action,
                    "matched_probe_random": random_action,
                    "reward_only_local": receipt.selected_action,
                    "oracle_hidden_train_map": oracle_action,
                }
                deploy_slice = slice(task.probe_trials, task.trials_per_block)
                deploy_fixed_accuracy = np.array(
                    [
                        binary_accuracy(
                            block.targets[deploy_slice],
                            _action_prediction(outputs, action)[deploy_slice],
                        )
                        for action in range(2)
                    ]
                )
                posthoc_best_action = int(np.argmax(deploy_fixed_accuracy))
                posthoc_full_accuracy = max(
                    binary_accuracy(block.targets, outputs.routing),
                    binary_accuracy(block.targets, outputs.associative),
                )
                pressure = (
                    spec.association_load
                    - 1
                    + task.distractor_strength**2 * spec.distractor_writes
                ) / float(task.key_dim)
                for mode in MODES:
                    prediction = np.asarray(predictions[mode], dtype=np.float64)
                    selected_action = int(selected_actions[mode])
                    full_accuracy = binary_accuracy(block.targets, prediction)
                    probe_accuracy = binary_accuracy(
                        block.targets[: task.probe_trials],
                        prediction[: task.probe_trials],
                    )
                    deploy_accuracy = binary_accuracy(
                        block.targets[deploy_slice], prediction[deploy_slice]
                    )
                    metrics = {
                        "status": "complete",
                        "profile": config["profile"],
                        "statistics_unit": "seed",
                        "nested_unit": "block",
                        "split_unit": "whole_block",
                        "time_points_randomly_split": False,
                        "training_algorithm": config["training_algorithm"],
                        "used_autograd": False,
                        "used_bptt": False,
                        "task_target_is_explicit_actuator_mixture": False,
                        "mode_or_demand_specific_gain_fitted": False,
                        "fixed_actuator_motifs_across_all_blocks": True,
                        "high_rank_carrier_present": False,
                        "carrier_dynamics_claimed": False,
                        "primary_scope": "full_test_block_including_probe_cost",
                        "probe_cost_in_primary": True,
                        "online_probe_feedback_available": True,
                        "selector_received_true_context": mode
                        == "oracle_hidden_train_map",
                        "selector_received_executed_scalar_reward": mode
                        == "reward_only_local",
                        "selector_received_unexecuted_reward": False,
                        "selector_received_candidate_utility_vector": False,
                        "selector_received_prospective_descriptor": False,
                        "reward_only_interface_audit": mode
                        != "reward_only_local"
                        or (
                            receipt.used_true_context is False
                            and receipt.used_counterfactual_reward is False
                        ),
                        "oracle_mapping_uses_train_blocks_only": True,
                        "fixed_best_uses_train_blocks_only": True,
                        "train_fixed_best_actuator": fixed_best,
                        "train_routing_accuracy": train_accuracy["routing"],
                        "train_associative_accuracy": train_accuracy["associative"],
                        "train_split_fingerprint": train_fingerprint,
                        "test_block_fingerprint": block.fingerprint,
                        "probe_schedule_fingerprint": probe_digest,
                        "selected_actuator": (
                            "associative_query_shuffled"
                            if mode == "associative_query_shuffled"
                            else ACTIONS[selected_action]
                        ),
                        "selected_action_index": selected_action,
                        "posthoc_best_deploy_actuator": ACTIONS[
                            posthoc_best_action
                        ],
                        "selected_posthoc_best": selected_action
                        == posthoc_best_action,
                        "full_block_accuracy": full_accuracy,
                        "probe_accuracy": probe_accuracy,
                        "deployment_accuracy": deploy_accuracy,
                        "full_block_regret_to_posthoc_fixed_oracle": (
                            posthoc_full_accuracy - full_accuracy
                        ),
                        "probe_trials": task.probe_trials,
                        "deployment_trials": task.trials_per_block
                        - task.probe_trials,
                        "interference_pressure": pressure,
                        "prediction_output_rms": float(
                            np.sqrt(np.mean(prediction**2))
                        ),
                        "functional_output_magnitude_matched": bool(
                            np.all(np.abs(prediction) == 1.0)
                        ),
                        "associative_write_l1": outputs.associative_budget.mean_update_l1,
                        "associative_write_l2": outputs.associative_budget.mean_update_l2,
                        "shuffled_write_l1": outputs.shuffled_budget.mean_update_l1,
                        "shuffled_write_l2": outputs.shuffled_budget.mean_update_l2,
                        "associative_final_state_l2": outputs.associative_budget.mean_final_state_l2,
                        "shuffled_final_state_l2": outputs.shuffled_budget.mean_final_state_l2,
                        "associative_query_shuffled_write_budget_exact": outputs.update_budget_exact,
                        "write_update_rank": outputs.associative_budget.per_write_rank,
                        "writes_per_trial": outputs.associative_budget.writes_per_trial,
                        "selector_update_l1": (
                            receipt.cumulative_update_l1
                            if mode == "reward_only_local"
                            else 0.0
                        ),
                        "selector_update_l2": (
                            receipt.cumulative_update_l2
                            if mode == "reward_only_local"
                            else 0.0
                        ),
                        "selector_q_routing": (
                            float(receipt.q_values[0])
                            if mode == "reward_only_local"
                            else q_prior
                        ),
                        "selector_q_associative": (
                            float(receipt.q_values[1])
                            if mode == "reward_only_local"
                            else q_prior
                        ),
                        "evidence_schema_version": EVIDENCE_SCHEMA_VERSION,
                        "run_git_commit": provenance["git"]["commit"],
                        "run_git_tree": provenance["git"]["tree"],
                        "run_git_dirty": provenance["git"]["dirty"],
                    }
                    run.record(metrics, **_spec_dimensions(spec, mode))
            except Exception as error:
                for mode in MODES:
                    run.mark_condition_failure(
                        error, **_spec_dimensions(spec, mode)
                    )
        return run.path


def _selected_seeds(config: dict[str, Any], override: str | None) -> Iterable[int]:
    return seed_list(override if override is not None else config["seeds"])


def main() -> None:
    parser = basic_parser(
        "Exp31 hidden-reliability reward-only actuator selector",
        "configs/smoke/exp31_hidden_reliability_reward_selector.json",
    )
    parser.add_argument("--run-label", help="path-safe panel label")
    args = parser.parse_args()
    config = load_json_config(args.config)
    for seed in _selected_seeds(config, args.seeds):
        path = run_seed(config, seed, args.results_root, run_label=args.run_label)
        print(path)


if __name__ == "__main__":
    main()
